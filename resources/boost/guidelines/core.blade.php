## Laravel FFMpeg

- `pbmedia/laravel-ffmpeg` provides a Laravel-integrated API for audio/video processing using FFmpeg, with support for format conversion, HLS streaming, frame extraction, watermarks, and filters.
- Always activate the `laravel-ffmpeg-development` skill when working with video/audio conversion, HLS exports, frame extraction, watermarking, or any code that uses the `FFMpeg` facade or `MediaOpener`.
