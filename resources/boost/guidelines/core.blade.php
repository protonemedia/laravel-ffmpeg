## Laravel FFMpeg

- `pbmedia/laravel-ffmpeg` provides an FFmpeg integration for Laravel, with support for conversions, HLS, watermarks, tiles, and frame exports using Laravel's Filesystem.
- Always activate the `laravel-ffmpeg-development` skill when working with audio/video conversions, HLS exports, frame extraction, watermarks, concatenation, or any code that uses the `FFMpeg` facade or `ProtoneMedia\LaravelFFMpeg` namespace.
