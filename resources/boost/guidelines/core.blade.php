## Laravel FFMpeg

- `protonemedia/laravel-ffmpeg` provides a Laravel-friendly wrapper around FFMpeg for transcoding, HLS streaming, frame extraction, watermarks, and more.
- Always activate the `laravel-ffmpeg-development` skill when working with video/audio processing, HLS exports, frame extraction, or any code that uses the `FFMpeg` facade or `ProtoneMedia\LaravelFFMpeg` classes.
